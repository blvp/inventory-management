                  </span>
                  <span v-else>-</span>
                </td>
              </tr>
            </tbody>
            <tbody v-if="monthlyData.length === 0">
              <tr><td colspan="5" class="empty-state">No monthly data available</td></tr>
            </tbody>
          </table>
        </div>
      </div>

      <!-- Summary Stats -->
      <div class="stats-grid">
        <div class="stat-card">
          <div class="stat-label">Total Revenue (YTD)</div>
          <div class="stat-value">${{ formatNumber(totalRevenue) }}</div>
        </div>
        <div class="stat-card">
          <div class="stat-label">Avg Monthly Revenue</div>
          <div class="stat-value">${{ formatNumber(avgMonthlyRevenue) }}</div>
        </div>
        <div class="stat-card">
          <div class="stat-label">Total Orders (YTD)</div>
          <div class="stat-value">{{ totalOrders }}</div>
        </div>
        <div class="stat-card">
          <div class="stat-label">Best Performing Quarter</div>
          <div class="stat-value">{{ bestQuarter }}</div>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import { ref, computed, watch, onMounted } from 'vue'
import { api } from '../api'
import { useFilters } from '../composables/useFilters'
import { useI18n } from '../composables/useI18n'

export default {
  name: 'Reports',
  setup() {
    const { t } = useI18n()
    const { selectedPeriod, selectedLocation, selectedCategory, selectedStatus, getCurrentFilters } = useFilters()

    const quarterlyData = ref([])
    const monthlyData = ref([])
    const loading = ref(true)
    const error = ref(null)

    const loadData = async () => {
      try {
        loading.value = true
        error.value = null
        const filters = getCurrentFilters()
        quarterlyData.value = await api.getQuarterlyReport(filters)
        monthlyData.value = await api.getMonthlyTrends(filters)
      } catch (err) {
        error.value = 'Failed to load reports: ' + err.message
      } finally {
        loading.value = false
      }
    }

    const formatNumber = (num) => {
      if (num == null || isNaN(num)) return '0.00'
      return Number(num).toLocaleString('en-US', { minimumFractionDigits: 2, maximumFractionDigits: 2 })
    }

    const formatMonth = (monthStr) => {
      if (!monthStr || typeof monthStr !== 'string') return 'Unknown'
      const parts = monthStr.split('-')
      if (parts.length < 2) return monthStr
      const monthIndex = parseInt(parts[1], 10) - 1
      const monthNames = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec']
      if (monthIndex < 0 || monthIndex > 11) return monthStr
      return `${monthNames[monthIndex]} ${parts[0]}`
    }

    const maxRevenue = computed(() => {
      if (monthlyData.value.length === 0) return 0
      return Math.max(...monthlyData.value.map(m => m.revenue ?? 0))
    })

    const getBarHeight = (revenue) => {
      if (maxRevenue.value === 0) return 0
      return Math.round((revenue / maxRevenue.value) * 200)
    }

    const totalRevenue = computed(() =>
      monthlyData.value.reduce((sum, m) => sum + (m.revenue ?? 0), 0)
    )

    const avgMonthlyRevenue = computed(() =>
      monthlyData.value.length > 0 ? totalRevenue.value / monthlyData.value.length : 0
    )

    const totalOrders = computed(() =>
      monthlyData.value.reduce((sum, m) => sum + (m.order_count ?? 0), 0)
    )

    const bestQuarter = computed(() => {
      if (quarterlyData.value.length === 0) return 'N/A'
      return quarterlyData.value.reduce((best, q) =>
        q.total_revenue > best.total_revenue ? q : best
      ).quarter
    })

    const getFulfillmentClass = (rate) => {
      if (rate >= 90) {
        return 'badge success'
      } else if (rate >= 75) {
        return 'badge warning'
      } else {
        return 'badge danger'
      }
    }

    const getChangeValue = (current, previous) => {
      const change = current - previous
      if (change > 0) {
        return '+$' + formatNumber(change)
      } else if (change < 0) {
        return '-$' + formatNumber(Math.abs(change))
      } else {
        return '$0.00'
      }
    }

    const getChangeClass = (current, previous) => {
      const change = current - previous
      if (change > 0) {
        return 'positive-change'
      } else if (change < 0) {
        return 'negative-change'
      } else {
        return ''
      }
    }

    const getGrowthRate = (current, previous) => {
      if (previous === 0) {
        return 'N/A'
      }
      const rate = ((current - previous) / previous) * 100
      const sign = rate > 0 ? '+' : ''
      return sign + rate.toFixed(1) + '%'
    }

    watch([selectedPeriod, selectedLocation, selectedCategory, selectedStatus], () => {
      loadData()
    })

    onMounted(() => {
      loadData()
    })

    return {
      t,
      quarterlyData,
      monthlyData,
      loading,
      error,
      totalRevenue,
      avgMonthlyRevenue,
      totalOrders,
      bestQuarter,
      loadData,
      formatNumber,
      formatMonth,
      getBarHeight,
      getFulfillmentClass,
      getChangeValue,
      getChangeClass,
      getGrowthRate
    }
  }
}
</script>

